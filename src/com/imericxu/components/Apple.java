package com.imericxu.components;

public class Apple
{
    private final int row;
    private final int col;
    
    public Apple(int row, int col)
    {
        this.row = row;
        this.col = col;
    }

    public int getRow()
    {
        return row;
    }
    
    public int getCol()
    {
        return col;
    }
}
